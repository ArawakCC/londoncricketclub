                <h2 id="sec0">Over 100 years of cricket in London Ontario</h2>
<p>London Cricket has its roots in the British Garrison stationed in London starting in 1838. Over the years the club has had many homes, including Tecumseh Park, Fanshawe Park, the London Psychiatric Hospital grounds, Maitland Park, and most recently the North London Athletic Fields.</p>
<p>We play league (UFCL) and friendly cricket in southwestern Ontario throughout the summer. New members welcome!</p>
                <p>Starting mid-May practices are held every Thursday and Friday at the North London Athletic Fields, starting at 6pm. Matches are played on weekends, typically starting at 1pm.</p>

