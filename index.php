                <h2 id="sec2">Membership</h2>
<p>Whether you want to play league, or just social cricket, or a bit of both, we would love to have you on board.</p>
                    <h3>Dues for 2015</h3>

<p>Full membership is $150 for most players. Students (including juniors) pay $100. For those who just want to play a few games and come to practices, we have a pavilion membership for $50. This gives you up to 5 games, but no league matches. Pavilion members can upgrade at any time to a full membership.</p>

<p>There is a per game match fee of $5. Occasionally, for special events, this may be more. Non-members, if selected, will also be asked to pay $10.</p>
<p>
Half the membership dues should be paid by the first game: the remainder is due by the end of May.
Payment can be made to any executive member by arrangement or during practice. Cheques should be made payable to London Cricket Club.
</p>
<p>
Please note that there will be no proration of membership dues for past and current members. In addition, each member is expected to share in the workload of the club and prepare tea at least once for a visiting team during the season. Any outstanding membership dues and game fees from previous seasons must be paid before your 2015 membership dues.
</p>


